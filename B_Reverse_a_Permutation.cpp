ll gcd(ll a, ll b)
{
    if (b == 0)
        return a;
    return gcd(b, a % b);
}

ll power(ll x, ll y)
{
    ll res = 1;
    if (x == 0)
        return 0;
    while (y > 0)
    {
        if (y & 1)
            res = (res * x);
        y = y >> 1;
        x = (x * x);
    }
    return res;
}
int x, dist;

void dfs(int node, int parent, int d, vector<vector<int>>& g) {
    if (d > dist) {
        dist = d;
        x = node;
    }

    for (auto child : g[node]) {
        if (child == parent) continue;
        dfs(child, node, d + 1, g);
    }
}

